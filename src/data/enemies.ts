export function getEnemiesForRoom(roomType: string, floor: number): Enemy[] {
  const typeMap: Record<string, string> = {
    'r-t1': 't1', 'r-t2': 't2', 'r-t3': 't3', 'r-t4': 't4',
    'r-boss': 'boss', 'r-final': 'final',
  };
  const enemyType = typeMap[roomType];
  if (!enemyType) return [];
  return ENEMIES.filter(e => e.floor === floor && e.type === enemyType);
}
